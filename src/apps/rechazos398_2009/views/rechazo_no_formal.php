<h1>DEVOLUCIONES DE VALORIZACIÓN POR ACUERDO 398 DE 2009 - INGRESO A LA PAGINA WEB</h1>
<p>La Dirección Técnica de Apoyo a la Valorización informa que mediante Resoluciones VA 2 - VA 7 del 30 de noviembre de 2007, se gravaron los predios beneficiarios de la Contribución de Valorización por Beneficio Local -Acuerdo 180 de 2005, ubicados en las localidades de Fontibon y Engativá.</p>

<p>Con fundamento en el Acuerdo 398 de 2009, se expidió la Resolución VA 016 de 29 de octubre de 2010, reasignando la Contribución de que se trata, a los predios ubicados en la Zona de Influencia 2 del grupo 1 del Sistema de Movilidad.</p>

<p>La norma Ibídem igualmente estableció, en el artículo 11 que el Instituto de desarrollo Urbano debía realizar las devoluciones correspondientes con valores indexados de conformidad a lo estatuido en el Estatuto Tributario Nacional.</p>

<p>En la Resolución 3244 del 21 de octubre de 2010, se definieron las reglas de reasignación y devolución, adoptándose mediante Resolución 3858 de 07 de diciembre de 2010, el procedimiento PR-CV-059 Devolución de Dinero por Concepto de Valorización” versión 1.0.</p>

<p>En ese orden de ideas el Protocolo de devoluciones consigna página No. 2:</p>

<blockquote>“<strong>…PRESENTACIÓN</strong> Este documento es el instructivo para el trámite de solicitudes de devolución radicadas en el IDU por los propietarios o poseedores de predios ubicados en la zona de influencia 2 del grupo 1, delimitada en el Acuerdo 180 de 2005. Constituye el protocolo o conjunto de reglas que deben cumplir las personas involucradas en el trámite de solicitudes de devolución de saldos crédito o saldos a favor de los contribuyentes.</blockquote>

<p>Las solicitudes de devolución originadas en la implementación del Acuerdo 398 de 2009 pueden iniciarse con el diligenciamiento de la información por parte del contribuyente a través de la Web y su remisión al IDU por este medio; o <strong>con el diligenciamiento de la información por parte del contribuyente a través de la Web y su radicación física en uno de los puntos autorizados por el IDU</strong>”. (Negrilla fuera del texto original)</p>

<p>Con base en lo enunciado, los contribuyentes que se relacionan a continuación realizaron un registro inicial en la página web del Instituto de Desarrollo Urbano; pero no consolidaron de forma definitiva la información la que era indispensable para realizar formalmente la solicitud de devolución.</p>

<p>Es de resaltar que al registro de la información vía Web, se le informó al contribuyente “<strong>ESTA SOLICITUD SOLO SE TRAMITARÁ SI TIENE EL ADHESIVO DE RADICACIÓN COLOCADO EN LOS PUNTOS DE ATENCIÓN IDU</strong>”.</p>

