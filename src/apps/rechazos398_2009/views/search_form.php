<h1>AVISO</h1>
<strong>ASUNTO:</strong> NOTIFICACIÓN POR AVISO<br />
<strong>REFERENCIA:</strong> RECHAZOS DE GIRO ACUERDO 398 DE 2009<br />
<strong>DEPENDENCIA:</strong> DIRECCIÓN TÉCNICA DE APOYO A LA VALORIZACIÓN<br />

<p>La Dirección Técnica de Apoyo a la Valorización en cumplimiento de la Ley 1437 de 18 de enero de 2011, lo preceptuado en el Decreto 019 de 2012 y ante la imposibilidad de lograr la comparecencia de los contribuyentes y la notificación personal prevista en el artículo 67 y 69 de Código de Procedimiento Administrativo y Contencioso Administrativo, luego de haberse realizado el envío de la comunicación citada a la dirección registrada por el peticionario de la devolución de la Contribución de Valorización ordenada mediante el Acuerdo 398 de 2009, se publica:
</p>
<p>
Que mediante el presente aviso se notifican los rechazos de giro de peticiones de devolución presentadas para el Acuerdo 398 de 2009, con el número de documento de identificación del beneficiario, número del radicado mediante el que se surtió el trámite, así como el número de la orden de pago. En este sentido se considera legalmente notificado el contribuyente al finalizar el día siguiente del retiro del presente aviso.
</p>
